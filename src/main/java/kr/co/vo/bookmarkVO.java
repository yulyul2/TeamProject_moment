package kr.co.vo;

public class bookmarkVO {

	private String mark_id;
	private int mark_no;
	private int mark_check;	
	
	
	public String getMark_id() {
		return mark_id;
	}
	public void setMark_id(String mark_id) {
		this.mark_id = mark_id;
	}
	public int getMark_no() {
		return mark_no;
	}
	public void setMark_no(int mark_no) {
		this.mark_no = mark_no;
	}
	public int getMark_check() {
		return mark_check;
	}
	public void setMark_check(int mark_check) {
		this.mark_check = mark_check;
	}

	
}
